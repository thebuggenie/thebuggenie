<div style="padding: 2px;"><b>What edition do I select?</b><br>
A project may be split into several "editions" - versions of the same project that may have different properties.
You should choose the edition that you are reporting an issue for. If you are unsure, just use the default, unless it
is clearly wrong.<br>  
<br>
<b>The edition I want to report an issue for is not on the list</b><br>
In this case, it might not be possible to report an issue for that edition. If so, try contacting the developers, either
by registering on the page and sending them a message, or look for a "contact us" link.<br>
